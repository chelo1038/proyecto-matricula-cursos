# creando un diccionario con dict()
#se pueden crear diccionarios vacios con esta funcion

diccionario = dict(nombre = " josue", apellido = "mora")

print(diccionario)

# creando diccionarios con .fromkeys()
# todos los valores sin definir 
# se usa con ([])


diccionario  = dict.fromkeys(["nombre","apellido"])
print(diccionario)

